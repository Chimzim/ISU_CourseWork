package api;

/**
 * Defines a set of constants for default values in a CricketGame.
 */
public class Defaults
{
  /**
   * Default number of bowls per over.
   */
  public static final int DEFAULT_BOWLS_PER_OVER = 6;
  
  /**
   * Default number of overs per innings.
   */
  public static final int DEFAULT_OVERS_PER_INNINGS = 50;
  
  /**
   * Default number of innings in the game.
   */
  public static final int DEFAULT_NUM_INNINGS = 2;
  
  /**
   * Default number of players on each side.
   */
  public static final int DEFAULT_NUM_PLAYERS = 11;
}